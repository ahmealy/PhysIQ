    dataset_dir              = 'data',
    batch_size               = 20,
    noise_std                = 2e-2,
    num_epochs               = 100,
    early_stopping_patience  = 10,
    lr                       = 1e-4,
    message_passing_num      = 15,
    checkpoint_dir           = 'checkpoints',
    log_dir                  = 'runs',
    # Derived from domain if not provided:
    output_size              = None,   # set automatically from domain: 2 (cylinder_flow) or 3 (flag_simple)
